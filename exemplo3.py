nota1 = float (input("Informe a 1º nota:")) # estamos convertendo os valores textuais em valores decimais
nota2 = float (input("Informe a 2º nota:"))
nota3 = float (input("Informe a 3º nota:"))

media = (nota1+nota2+nota3)/3

print(f"Sua média é {media:.2f}") #.2f significa 2 casas decimais após a virgula