valorA = int(input("Informe um valor numérico A: "))
valorB = int(input("Informe um valor numérico B: "))
valorC = int(input("Informe um valor numérico C: "))

if valorA+valorB<valorC:
    print(f"O número {valorA+valorB} é menor do que {valorC}\n")
else:
    print(f"O número {valorA+valorB} é maior do que {valorC}\n")